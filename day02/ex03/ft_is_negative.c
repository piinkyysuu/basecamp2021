#include <unistd.h>

void	ft_putchar(char c);

void	ft_is_negative(int n)
{
	if (n < 0)
	{
		ft_putchar('N');
	}
	if (n > 0 || n == 'NULL')
	{
		ft_putchar('P');
	}
	ft_putchar('\n');
}
