int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int acc;

	i = 0;
	acc = 0;
	while (s1[i] != '\0')
		acc += s1[i++];
	if (!acc)
		return (0 - s2[0]);
	i = 0;
	while (s1[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
