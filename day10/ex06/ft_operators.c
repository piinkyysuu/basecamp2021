int		ft_mutiply(int x, int y)
{
	return (x * y);
}

int		ft_addition(int x, int y)
{
	return (x + y);
}

int		ft_subtract(int x, int y)
{
	return (x - y);
}

int		ft_divide(int x, int y)
{
	return (x / y);
}

int		ft_modulus(int x, int y)
{
	return (x % y);
}
